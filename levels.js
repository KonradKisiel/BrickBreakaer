//sum all numbers in an array
function sumArray(inputArray){
	var sum = 0;
	for(i=0; i<inputArray.length; i++){
        for(j=0; j<inputArray[i].length; j++){
            if(inputArray[i][j] !== 0){
		        sum += inputArray[i][j];
            }
        }
	}
	return sum;
}
